import type { SVGProps, ReactNode } from "react";

type IconProps = SVGProps<SVGSVGElement> & {
  size?: "xs" | "sm" | "md" | "lg" | "xl";
};

const sizeClasses = {
  xs: "w-3 h-3",
  sm: "w-4 h-4",
  md: "w-5 h-5",
  lg: "w-6 h-6",
  xl: "w-8 h-8",
};

function BaseIcon({
  children,
  size = "md",
  className = "",
  ...props
}: IconProps & { children: ReactNode }) {
  const sizeClass = sizeClasses[size];
  return (
    <svg
      className={`${sizeClass} ${className}`}
      fill="none"
      stroke="currentColor"
      viewBox="0 0 24 24"
      {...props}
    >
      {children}
    </svg>
  );
}

